package TileObject;

public class Point {
    private final int xLocation;
    private final int yLocation;

    public Point(int xLocation, int yLocation) {
        this.xLocation = xLocation;
        this.yLocation = yLocation;
    }

    public int getX() {
        return xLocation;
    }

    public int getY() {
        return yLocation;
    }
}


